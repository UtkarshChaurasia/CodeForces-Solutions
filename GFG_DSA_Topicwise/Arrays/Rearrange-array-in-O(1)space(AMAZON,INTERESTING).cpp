void solve()
{
	ll i,j,n,m,a,b,rem,count=0,sum=0;
	cin>>n;
	vl arr(n);
	fo(i,n)
	{
	    cin>>arr[i];
	}
	ll maxi = n-1;
	ll mini = 0;
	ll max = arr[n-1]+1;
	fo(i,n)
	{
	    if((i%2)==0)
	    {
	        arr[i] = arr[i] + (arr[maxi] % max)*max;
	        maxi--;
	    }
	    else
	    {
	        arr[i] = arr[i] + (arr[mini] % max)*max;
	        mini++;
	    }
	}
	fo(i,n)
	{
	    cout<<arr[i]/max<<" ";
	}
	cout<<endl;
}





int mpow(int base, int exp)
{
	base %= MOD;
	int result = 1;
	while(exp>0)
	{
		if(exp & 1)
		{
			result = ((ll)result*base);
		}
		
		base = ((ll)base*base) % MOD;
		exp >>= 1;
	}
	
	return result;
}




